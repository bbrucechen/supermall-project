<template>
  <div class="toast" v-show="isShow">
      <div>{{message}}</div>
    </div>   
</template>

<script>
export default {
    name:"Toast",
    data() {
        return {
            message:'',
            isShow:false
        }
    },
    methods:{
        show(message,duration = 1500) {
            console.log(111);
            
            this.isShow = true;
            this.message = message;

            setTimeout(() => {
                this.isShow = false;
            },duration)
        }
    }
}
</script>

<style>
    .toast {
        position: fixed;
        top: 50%;
        left: 50%;
        transform:translate(-50%,-50%);
        padding:8px 10px;
        background-color: rgba(0, 0, 0, .8);
        z-index: 1000000000;
    }
</style>